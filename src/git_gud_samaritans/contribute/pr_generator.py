        """
        Submit a pull request to GitHub.

        Args:
            repo: Repository full name (owner/repo)
            pr_details: PR details to submit

        Returns:
            URL of the created PR
        """
        logger.info(
            "submitting_pr", repo=repo, branch=pr_details.branch, title=pr_details.title
        )

